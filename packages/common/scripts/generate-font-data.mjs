#!/usr/bin/env node
/**
 * Generates fontData.ts with base64-encoded font constants
 * Reads all TTF/OTF files from assets/fonts/ (excluding ArialUnicodeMS)
 * and creates TypeScript constants for use in helper.ts
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const fontsDir = path.resolve(__dirname, '../assets/fonts');
const outputFile = path.resolve(__dirname, '../src/fontData.ts');

// Font metadata: maps family name to list of variant files
const fontFamilies = {
  Arial: ['Arial.ttf', 'ArialBlack.ttf', 'ArialBold.ttf', 'ArialBoldItalic.ttf', 'ArialItalic.TTF'],
  AvenirBook: ['AvenirBook.ttf'],
  BaroqueAntiqueScript: ['BaroqueAntiqueScript.ttf'],
  CalibriFontFamily: ['CalibriRegular.ttf', 'CalibriBold.ttf', 'CalibriItalic.ttf', 'CalibriBoldItalic.ttf'],
  GelatoScript: ['GelatoScript.ttf'],
  ITCTempus: ['ITCTempus.ttf'],
  LucidaCalligraphy: ['LucidaCalligraphy.ttf'],
  MonotypeCorsiva: ['MonotypeCorsivaRegular.ttf', 'MonotypeCorsivaBold.ttf', 'MonotypeCorsivaRegularItalic.ttf', 'MonotypeCorsivaBoldItalic.ttf'],
  RumbleBrave: ['RumbleBrave.otf'],
  Tahoma: ['Tahoma.ttf'],
  TimesNewRoman: ['TimesNewRoman.ttf', 'TimesNewRomanBold.ttf', 'TimesNewRomanItalic.ttf', 'TimesNewRomanBoldItalic.ttf'],
  // ArialUnicodeMS is deliberately excluded (too large: 39MB combined)
};

// Helper: convert filename to constant name suffix
function getConstantSuffix(str) {
  return str
    .replace(/\.(ttf|otf|TTF|OTF)$/, '')
    .replace(/([A-Z]+)/g, match => '_' + match)
    .replace(/^_/, '')
    .toUpperCase();
}

console.log('🔤 Generating font data constants...');

const constants = [];

for (const [familyName, filenames] of Object.entries(fontFamilies)) {
  for (const filename of filenames) {
    const filePath = path.join(fontsDir, familyName, filename);

    if (!fs.existsSync(filePath)) {
      console.warn(`⚠️  File not found: ${filePath}`);
      continue;
    }

    const buffer = fs.readFileSync(filePath);
    const base64 = buffer.toString('base64');
    // Simpler naming: FAMILY_NAME_VARIANT_FONT_VALUE
    const familyPart = familyName.replace(/FontFamily/, '').replace(/([A-Z])/g, '_$1').replace(/^_/, '').toUpperCase();
    const filenamePart = filename
      .replace(/\.(ttf|otf|TTF|OTF)$/, '')
      .replace(/([A-Z])/g, '_$1')
      .replace(/^_/, '')
      .toUpperCase();
    const constantName = `${familyPart}_${filenamePart}_FONT_VALUE`.replace(/__+/g, '_').replace(/_FONT_VALUE$/, '_FONT_VALUE');

    constants.push({
      name: constantName,
      value: base64,
      source: `${familyName}/${filename}`,
      size: buffer.length,
    });

    console.log(`✓ ${familyName}/${filename} (${(buffer.length / 1024 / 1024).toFixed(2)}MB) -> ${constantName}`);
  }
}

// Generate TypeScript file
let tsContent = '/**\n * Auto-generated font data constants\n * Generated by: packages/common/scripts/generate-font-data.mjs\n * DO NOT EDIT MANUALLY\n */\n\n';

// Add constants
for (const constant of constants) {
  tsContent += `export const ${constant.name} = '${constant.value}';\n`;
}

// Write file
fs.writeFileSync(outputFile, tsContent);

const totalSize = constants.reduce((sum, c) => sum + c.size, 0);
const totalBase64Size = constants.reduce((sum, c) => sum + c.value.length, 0);

console.log(`\n✨ Generated ${outputFile}`);
console.log(`   Total fonts: ${constants.length}`);
console.log(`   Binary size: ${(totalSize / 1024 / 1024).toFixed(2)}MB`);
console.log(`   Base64 size: ${(totalBase64Size / 1024 / 1024).toFixed(2)}MB`);
console.log(`   File size: ${(fs.statSync(outputFile).size / 1024 / 1024).toFixed(2)}MB`);
